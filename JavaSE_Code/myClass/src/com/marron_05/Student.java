package com.marron_05;

public class Student {
    private String name;
    private int age;

    // get/set方法
    public void getName(String n) {
        name = n;
    }

    public String getName() {
        return name;
    }

    public void setAge(int a) {
        age = a;
    }

    public int getAge() {
        return age;
    }
}
